def say_hello():
    print("Hello World")

# ფუნქციის გამოძახება ხდება მისი სახელის დაწერით და ფრჩხილებით
say_hello()


# ფუნქცია შეიძლება იღებდეს პარამეტრებს
# პარამეტრები არის ცვლადები, რომლებსაც ფუნქცია იღებს გარედან

def greet(name):
    print("Hello", name)

greet("Giorgi")
greet("Nika")


# ფუნქცია შეიძლება აბრუნებდეს მნიშვნელობას return-ის გამოყენებით
# return აგზავნის შედეგს ფუნქციის გარეთ

def add(a, b):
    return a + b

result = add(5, 3)
print(result)


# ფუნქცია შეიძლება გამოიყენებოდეს სხვადასხვა მონაცემებზე
# რაც ამცირებს კოდის გამეორებას

def multiply(x, y):
    return x * y

print(multiply(4, 5))
