# ფუნქციის შექმნა ხდება def საკვანძო სიტყვით
# def-ის შემდეგ ვწერთ ფუნქციის სახელს
# შემდეგ ფრჩხილებში ვწერთ პარამეტრებს (თუ საჭიროა)
